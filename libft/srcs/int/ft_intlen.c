int	ft_intlen(long int nb)
{
	int	i;

	i = 1;
	if (nb < 0)
	{
		nb = -nb;
		i++;
	}
	while (nb > 9)
	{
		i++;
		nb = nb / 10;
	}
	return (i);
}
